public class StringExercise {
    public static void main(String[] args) {
        // Escape Sequences
        String messageWithQuotes = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(messageWithQuotes);

        String windowsDrive = "In windows, the main drive is usually C:\\";
        System.out.println(windowsDrive);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}



